def lookup_annex_c(advancing_3rd_groups):
    """Given the set of 8 groups whose 3rd-place team advanced (e.g. {'A','C','D','E','F','H','I','L'}),
    return a dict {match_id: source_group} indicating which group's 3rd-place
    team plays in each of the 8 R32 slots.

    Raises KeyError if the combination isn't found in Annex C.
    """
    key = tuple(sorted(advancing_3rd_groups))
    for groups, slots in ANNEX_C_ROWS:
        if tuple(sorted(groups)) == key:
            return dict(zip(ANNEX_C_COLUMNS_TO_MATCH, slots))
    raise KeyError(f"Annex C lookup failed for {key}")


